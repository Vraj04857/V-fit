    // UC-07: Auto-generate workout plan
    @Transactional
    public WorkoutPlanDto generateWorkoutPlan(Long userId) {

        User user = userRepository.findById(userId)
                .orElseThrow(() -> new RuntimeException("User not found"));

        Profile profile = profileRepository.findByUser_UserId(userId)
                .orElseThrow(() -> new RuntimeException(
                        "Please complete your profile first"));

        // Validate required profile fields
        if (profile.getFitnessGoal() == null || profile.getActivityLevel() == null) {
            throw new RuntimeException(
                    "Fitness goal and activity level are required");
        }

        WorkoutPlan plan = new WorkoutPlan();
        plan.setUser(user);
        plan.setPlanName("Auto-Generated: " + profile.getFitnessGoal());
        plan.setGoalCategory(profile.getFitnessGoal());
        plan.setScheduleCalendar("WEEKLY");
        plan.setIsAutoGenerated(true);

        // Generate exercises based on goal
        List<Exercise> exercises = generateExercisesForGoal(
                plan, profile.getFitnessGoal(), profile.getActivityLevel());

        if (exercises.isEmpty()) {
            throw new RuntimeException(
                    "No exercises available. Please contact admin.");
        }

        plan.setExercises(exercises);
        WorkoutPlan saved = workoutPlanRepository.save(plan);

        return workoutService.mapToDto(saved);
    }

